/*
    operator condisional (?) kita menentukan hasilnya true / false
    contoh 
   "(kondisi) ? benar : salah" 
    jika kondisinya benar dia akan mengambil nilai sebelah kiri (:) jika nilainya
    salah dia akan mengambil nilai  setelah (:)
*/

//contoh ini untuk mengecek bilangan ganjil dan genap
var x = 10;
console.log( x % 2 == 0 ? "genap" : "ganjil");

var b = 2;
console.log(10 < b ? "benar" : "salah");

